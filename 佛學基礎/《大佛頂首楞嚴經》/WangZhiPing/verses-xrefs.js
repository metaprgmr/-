function w2hyDisp(volN, verseN) { // both 1-based
  var hy = W2HY[volN + '-' + verseN];
  return !hy ? '' : ((hy.length === 1) ? hy[0] : (hy[0] + '<br>' + hy[1]));
}
function h2w(volN, verseN) { // both 1-based
  if (typeof verseN == 'string')
    while (verseN.startsWith('0')) verseN = verseN.substring(1);
  return H2W[volN + '-' + verseN];
}

